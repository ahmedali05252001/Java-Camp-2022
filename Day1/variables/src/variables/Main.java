package variables;

public class Main {

	public static void main(String[] args) {
		
		// Variables (Değişkenler)
		// Değişkenlerin adlandırılmasında camelCase standardı kullanılır.
		// Yani; değişkenin ilk kelimesi küçük, diğer kelimelerin ilkharfleri büyük yazılır)
		// Örn: ogrenciSayisi
		
		int ogrenciSayisi = 12;
		String mesaj = "Öğrenci Sayısı : ";
		System.out.println(mesaj + ogrenciSayisi);
		System.out.println("Öğrenci Sayım : " + ogrenciSayisi);
	}

}
